
// Your function takes two arguments:

// current father's age (years)
// current age of his son (years)
// Ð¡alculate how many years ago the father was twice as old as his 
// son (or in how many years he will be twice as old).

const twiceAsOld = (dadYearsOld, sonYearsOld) => {
    sonTwiceAge = sonYearsOld * 2;

    let fatherWasTwiceYearsAgo = dadYearsOld - sonTwiceAge;

    return Math.abs(fatherWasTwiceYearsAgo);
}

